# 当前语言设置（默认中文）
_current_language = "zh_CN"


def set_language(lang: str):
    """设置当前语言"""
    global _current_language
    if lang in TEXTS:
        _current_language = lang
    else:
        raise ValueError(f"Unsupported language: {lang}")


def get_language():
    """获取当前语言"""
    return _current_language


def is_english():
    """判断当前是否为英文"""
    return _current_language == "en_US"


def t(key: str, **kwargs) -> str:
    """
    获取翻译文本

    Args:
        key: 文本键
        **kwargs: 格式化参数（用于消息模板）

    Returns:
        翻译后的文本
    """
    text = TEXTS.get(_current_language, {}).get(key, key)
    if kwargs:
        try:
            text = text.format(**kwargs)
        except (KeyError, IndexError):
            pass
    return text


def get_all_keys():
    """获取所有文本键（用于调试）"""
    return list(TEXTS.get("zh_CN", {}).keys())
